package com.BeyondVerbal.comm.data.response;

public class AnalysisSegment {
	public int offset;
	public int duration;
	public Analysis analysis;
	/* (non-Javadoc)
	 * @see java.lang.Object#toString()
	 */
	@Override
	public String toString() {
		return "AnalysisSegment [offset=" + offset
				+ ", duration=" + duration + ", analysis=" + analysis + "]";
	}
	
	
}
